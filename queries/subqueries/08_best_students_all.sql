-- ============================================
-- ЗАДАЧА 4.3: Студенты с минимальной оценкой выше среднего по всем студентам
-- Упрощённый вариант
-- ============================================

\echo '=== СТУДЕНТЫ, У КОТОРЫХ МИНИМАЛЬНАЯ ОЦЕНКА ВЫШЕ ОБЩЕГО СРЕДНЕГО ==='

SELECT 
    СТ.фамилия || ' ' || СТ.имя AS студент,
    ROUND(AVG(У.оценка), 2) AS средний_балл,
    COUNT(У.оценка) AS количество_оценок,
    MIN(У.оценка) AS мин_оценка,
    MAX(У.оценка) AS макс_оценка
FROM СТУДЕНТЫ СТ
JOIN УСПЕВАЕМОСТЬ У ON У.студент = СТ.код
GROUP BY СТ.фамилия, СТ.имя, СТ.код
HAVING MIN(У.оценка) > (SELECT AVG(оценка) FROM УСПЕВАЕМОСТЬ)
ORDER BY средний_балл DESC
LIMIT 20;
