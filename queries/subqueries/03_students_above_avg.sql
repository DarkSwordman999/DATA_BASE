-- ============================================
-- ЗАДАЧА 2.1: Студенты со средним баллом выше среднего по дисциплине
-- Подзапросы в роли ТАБЛИЦ
-- ============================================

\echo '=== СТУДЕНТЫ СО СРЕДНИМ БАЛЛОМ ВЫШЕ СРЕДНЕГО ПО ДИСЦИПЛИНЕ ==='

SELECT 
    студент,
    дисциплина,
    средний_балл_студента,
    средний_балл_дисциплины
FROM (
    SELECT 
        СТ.фамилия || ' ' || СТ.имя AS студент,
        Д.название AS дисциплина,
        AVG(У.оценка) AS средний_балл_студента,
        (SELECT AVG(У2.оценка) FROM УСПЕВАЕМОСТЬ У2 WHERE У2.дисциплина = Д.код) AS средний_балл_дисциплины
    FROM УСПЕВАЕМОСТЬ У
    JOIN СТУДЕНТЫ СТ ON СТ.код = У.студент
    JOIN ДИСЦИПЛИНЫ Д ON Д.код = У.дисциплина
    GROUP BY СТ.фамилия, СТ.имя, Д.название, Д.код
) AS subquery
WHERE средний_балл_студента > средний_балл_дисциплины
ORDER BY дисциплина, средний_балл_студента DESC
LIMIT 20;
