-- ============================================
-- ЗАДАЧА 3.1: Топ-3 лучших студента на каждом факультете
-- Подзапросы в роли СВЯЗАННЫХ ТАБЛИЦ (LATERAL)
-- ============================================

\echo '=== ТОП-3 ЛУЧШИХ СТУДЕНТА НА КАЖДОМ ФАКУЛЬТЕТЕ ==='

SELECT 
    факультет,
    студент,
    средний_балл,
    место
FROM (
    SELECT DISTINCT ГР.факультет 
    FROM ГРУППЫ ГР
) AS факультеты,
LATERAL (
    SELECT 
        СТ.фамилия || ' ' || СТ.имя AS студент,
        ROUND(AVG(У.оценка), 2) AS средний_балл,
        ROW_NUMBER() OVER (ORDER BY AVG(У.оценка) DESC) AS место
    FROM СТУДЕНТЫ СТ
    JOIN УСПЕВАЕМОСТЬ У ON У.студент = СТ.код
    JOIN ГРУППЫ ГР ON ГР.код = У.группа
    WHERE ГР.факультет = факультеты.факультет
    GROUP BY СТ.фамилия, СТ.имя
    ORDER BY средний_балл DESC
    LIMIT 3
) AS top
ORDER BY факультет, место;
