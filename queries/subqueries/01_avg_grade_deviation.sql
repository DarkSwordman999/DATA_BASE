-- ============================================
-- ЗАДАЧА 1.1: Отклонение среднего балла студента от среднего по группе
-- Подзапросы в роли ПОЛЕЙ
-- ============================================

\echo '=== ОТКЛОНЕНИЕ СРЕДНЕГО БАЛЛА СТУДЕНТА ОТ СРЕДНЕГО ПО ГРУППЕ ==='

SELECT 
    СТ.фамилия || ' ' || СТ.имя AS студент,
    ГР.название AS группа,
    (SELECT AVG(оценка) FROM УСПЕВАЕМОСТЬ WHERE студент = СТ.код) AS средний_балл_студента,
    (SELECT AVG(У.оценка) FROM УСПЕВАЕМОСТЬ У WHERE У.группа = ГР.код) AS средний_балл_группы,
    ROUND(
        COALESCE((SELECT AVG(оценка) FROM УСПЕВАЕМОСТЬ WHERE студент = СТ.код), 0) - 
        COALESCE((SELECT AVG(У.оценка) FROM УСПЕВАЕМОСТЬ У WHERE У.группа = ГР.код), 0), 2
    ) AS отклонение
FROM СТУДЕНТЫ СТ
CROSS JOIN ГРУППЫ ГР
WHERE (SELECT COUNT(*) FROM УСПЕВАЕМОСТЬ WHERE студент = СТ.код) > 0
GROUP BY СТ.фамилия, СТ.имя, СТ.код, ГР.название, ГР.код
ORDER BY отклонение DESC
LIMIT 10;
