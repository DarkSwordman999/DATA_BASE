\echo '=== СТАТИСТИКА УСПЕВАЕМОСТИ ПО ФАКУЛЬТЕТАМ ==='

SELECT 
    ГР.факультет,
    COUNT(DISTINCT У.студент) AS студентов,
    COUNT(DISTINCT У.дисциплина) AS дисциплин,
    ROUND(AVG(У.оценка), 2) AS средний_балл
FROM УСПЕВАЕМОСТЬ У
JOIN ГРУППЫ ГР ON ГР.код = У.группа
GROUP BY ГР.факультет
ORDER BY средний_балл DESC;
