-- ============================================
-- ЗАДАЧА 1.2: Рейтинг преподавателя относительно общего среднего
-- Подзапросы в роли ПОЛЕЙ
-- ============================================

\echo '=== РЕЙТИНГ ПРЕПОДАВАТЕЛЕЙ ==='

SELECT 
    ПР.фамилия || ' ' || ПР.имя AS преподаватель,
    (SELECT AVG(оценка) FROM УСПЕВАЕМОСТЬ WHERE преподаватель = ПР.код) AS средний_балл,
    (SELECT AVG(оценка) FROM УСПЕВАЕМОСТЬ) AS общий_средний,
    ROUND(
        (SELECT AVG(оценка) FROM УСПЕВАЕМОСТЬ WHERE преподаватель = ПР.код) - 
        (SELECT AVG(оценка) FROM УСПЕВАЕМОСТЬ), 2
    ) AS отклонение
FROM ПРЕПОДАВАТЕЛИ ПР
WHERE (SELECT COUNT(*) FROM УСПЕВАЕМОСТЬ WHERE преподаватель = ПР.код) > 0
ORDER BY средний_балл DESC
LIMIT 10;
