-- ============================================
-- ЗАДАЧА 4.2: Студенты, сдавшие все экзамены на 4 и 5
-- Использование NOT EXISTS
-- Подзапросы в роли УСЛОВИЯ
-- ============================================

\echo '=== СТУДЕНТЫ, СДАВШИЕ ВСЕ ЭКЗАМЕНЫ НА 4 И 5 ==='

SELECT 
    СТ.фамилия || ' ' || СТ.имя AS студент,
    COUNT(У.оценка) AS количество_экзаменов,
    ROUND(AVG(У.оценка), 2) AS средний_балл
FROM СТУДЕНТЫ СТ
JOIN УСПЕВАЕМОСТЬ У ON У.студент = СТ.код
WHERE NOT EXISTS (
    SELECT 1 FROM УСПЕВАЕМОСТЬ У2 
    WHERE У2.студент = СТ.код AND У2.оценка < 4
)
GROUP BY СТ.фамилия, СТ.имя, СТ.код
ORDER BY средний_балл DESC
LIMIT 20;
